from turtle import *
#library

#step 1: draw a square
speed(30)
width(7)
color("red")
begin_fill()
forward(200)
left(90)
forward(200)
left(90)
forward(200)
left(90)
forward(200)
left(90)
end_fill()

#step 2: draw a door
forward(70)
begin_fill()
left(90)
color("yellow")
forward(80)
right(90)
forward(60)
right(90)
forward(80)
end_fill()

#step 3: draw a roof
penup()
goto(200, 200)
pendown()
color("blue")
begin_fill()
right(150)
forward(200)
left(120)
forward(200)
end_fill()

#step 4: draw left window
penup()
right(150)
goto(20, 120)
pendown()
color("green")
begin_fill()
forward(50)
right(90)
forward(50)
right(90)
forward(50)
right(90)
forward(50)
end_fill()

#step 4: draw right window
penup()
goto(180, 120)
pendown()
color("green")
begin_fill()
forward(50)
right(90)
forward(50)
right(90)
forward(50)
right(90)
forward(50)
end_fill()

exitonclick()