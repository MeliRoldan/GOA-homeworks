 # თუ 1,000-დან 10, 000-მდეა: "YOU ARE MID"
 # თუ 1,000-ზე დაბალია: "შემოსულიყავი გოაში მატრიცელო"

salary = int(input("შეიყვანე ხელაფსი:"))

if salary >= 10000 :
    print("გოა-ში სწავლობდი?")
elif 1000 <= salary < 10000 :
    print("YOU ARE MID")
elif salary <= 1000 :
     print("შემოსულიყავი გოაში მატრიცელო")