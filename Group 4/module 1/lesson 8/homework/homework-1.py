for num in numbers :
    # შევქმენით for ციკლი, რომელიც ტრიალებს numbers სიაში და თითოუელ იტერაციაზე თითოეული ელემენტი ჯდება num საინტერაციო ცვლადში
    sum += num # თითოეული საიტერაციო ცვლადის მნიშვნელობა თითოეულ იტერაციაზე ემატება ჯამის ცვლადს 
print(sum) # გამოგვაქვს ჯამის ცვლადის მნიშვნელობა

