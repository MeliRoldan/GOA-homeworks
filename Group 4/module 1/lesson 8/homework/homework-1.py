numbers = [10, 2, 23, 64, 45, 36, 5] # ვადეკლარირებთ სიის ცვლადს სახელით numbers და ტოლობის ნიშნით ვანიჭებთ მნიშვენელობას კვადრატულ ფრჩხილებით შექმნილი სიის სახით, სადაც ელემენტები გამოყოფილია მძიმით

