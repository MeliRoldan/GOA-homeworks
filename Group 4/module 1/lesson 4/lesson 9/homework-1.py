# იპოვეთ ყველაზე პატარა რიცხვი

nums = [10, 2, 5, 20, 1]
smallest = nums[0] # შევქმნათ უმცირესის ცვლადი რომელსაც მივანიჭებთ სიის პირველ ელემენტს, რომელსაც შევადარებთ დანარჩენს ელემენეტებს

for i in nums:
    if i < smallest:
        smallest = i # ანიჭებს თითოეულ იტერაციაზე ნაპოვნ უმცირეს ელემენტს უმცირესის ცვლადს

print("The smallest number is:", smallest)